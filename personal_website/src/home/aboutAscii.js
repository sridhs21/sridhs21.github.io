// Character weight groups — swaps only happen within the same visual "density"
const LIGHT = ['.', ':', ','];
const MEDIUM = ['-', '~', '=', '+'];
const HEAVY = ['*', '#', '%', '@', '&'];

function getGroup(ch) {
  if (LIGHT.includes(ch)) return LIGHT;
  if (MEDIUM.includes(ch)) return MEDIUM;
  if (HEAVY.includes(ch)) return HEAVY;
  return null;
}

const ROWS = ABOUT_ASCII.split('\n');
const TOTAL_ROWS = ROWS.length;
const MAX_COLS = Math.max(...ROWS.map(r => r.length));

// Build a flat index of all non-space character positions for fast random picking
const CHAR_POSITIONS = [];
for (let r = 0; r < TOTAL_ROWS; r++) {
  for (let c = 0; c < ROWS[r].length; c++) {
    if (ROWS[r][c] !== ' ') {
      CHAR_POSITIONS.push({ r, c, orig: ROWS[r][c] });
    }
  }
}

// How many characters to "flicker" each frame
const FLICKER_COUNT = Math.max(8, Math.floor(CHAR_POSITIONS.length * 0.02));

function AnimatedAscii() {
  const buildGrid = useCallback(() => {
    return ROWS.map(row =>
      row.split('').map(ch => ({ ch, opacity: 1 }))
    );
  }, []);

  const [grid, setGrid] = useState(buildGrid);
  const frameRef = useRef(null);
  const lastTime = useRef(0);

  useEffect(() => {
    const INTERVAL = 120; // ms between flicker frames

    const tick = (now) => {
      if (now - lastTime.current >= INTERVAL) {
        lastTime.current = now;

        setGrid(prev => {
          const next = [...prev];
          const touchedRows = new Set();

          for (let i = 0; i < FLICKER_COUNT; i++) {
            const pos = CHAR_POSITIONS[Math.floor(Math.random() * CHAR_POSITIONS.length)];
            const group = getGroup(pos.orig);

            if (!touchedRows.has(pos.r)) {
              next[pos.r] = [...prev[pos.r]];
              touchedRows.add(pos.r);
            }

            if (group && Math.random() < 0.6) {
              const swap = group[Math.floor(Math.random() * group.length)];
              next[pos.r][pos.c] = {
                ch: swap,
                opacity: 0.4 + Math.random() * 0.6,
              };
            } else {
              next[pos.r][pos.c] = {
                ch: pos.orig,
                opacity: 0.3 + Math.random() * 0.7,
              };
            }
          }

          const restoreCount = Math.floor(FLICKER_COUNT * 1.2);
          for (let i = 0; i < restoreCount; i++) {
            const pos = CHAR_POSITIONS[Math.floor(Math.random() * CHAR_POSITIONS.length)];
            if (!touchedRows.has(pos.r)) {
              next[pos.r] = [...prev[pos.r]];
              touchedRows.add(pos.r);
            }
            next[pos.r][pos.c] = { ch: pos.orig, opacity: 1 };
          }

          return next;
        });
      }
      frameRef.current = requestAnimationFrame(tick);
    };

    frameRef.current = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(frameRef.current);
  }, []);

  return (
    <pre className="hm__ascii-art hm__ascii-art--animated" aria-label="ASCII art of Swaroop and his dog">
      {grid.map((row, r) => (
        <div key={r} className="hm__ascii-row">
          {row.map((cell, c) =>
            cell.ch === ' ' ? (
              <span key={c}> </span>
            ) : (
              <span
                key={c}
                style={{ opacity: cell.opacity }}
                className="hm__ascii-char"
              >
                {cell.ch}
              </span>
            )
          )}
        </div>
      ))}
    </pre>
  );
}

